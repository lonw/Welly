      <div class="modal-body"><h4><b>加拿大投资房地产的好渠道—REITs</b></h4><br>
        <b>一、REITs是什么? </b>
        <p>&nbsp;&nbsp;私人贷款由借款人的物业作为抵押物，并且对项目的贷款与价值比例（LTV）进行严格控制。</p>
        <p>所谓REITs(real estate investment trust)，就是一种以发行收益凭证的方式，汇集投资者的资金，由专门投资机构进行房地产投资、经营、管理，
          并将投资综合收益按比例分配给投资者的一种信託基金。REITs投资的房地产涵盖各种行业，如购物中心、办公大楼、停车场、酒店、医院、养老院等，
          其潜在收益来自于租金收入以及房地产价格增值的收入。 本 自  REITs起源于美国，自1960年以来已有40多年歷史。目前在美国大约有190多家上市交易的REITs，
          总资产约4390亿美元，这些REITs在主要交易所上市交易。加拿大的第一只REIT在1993年推出，20年后的今天，
          发展到40多家REITs，最大5家REITs的市值，占加拿大REITs市场的一半。RioCan是加拿大最大的REIT，在加拿大拥有接近300个商场、在美国拥有31个商场的股权，目前收益率是5.6%。</p>
          <b>二、投资 REITs有哪些优势？</b>
        <p>
          &nbsp;&nbsp;
          REITs 具备收益稳定、分散风险、流动性强、税务优惠等优势。<br>
          1. 收益稳定<br>
          REITs的现金流，主要来自租金收入、管理维修费用及承租率等，收入相对稳定，加拿大的REITs通常将当年度75-90%的收益以股息的形式分配给股东，
          所以投资REITs每年将享有稳定的现金收入。 
          2. 分散风险<br>
          REITs一般是由专业的房地产公司发起并管理的，能够合理地选择投资的项目，并能对其进行科学的管理，从经营管理上大降低了投资风险。其次由于基金的规模比较大，因而能广泛投资于各种类型的房地产专案，从而分散了投资风险。
          此外，REITs与其他股市、债市资产的相关系数低，在资产配置中加入REITs，可以降低整个投资组合的风险<br>
          3. 流动性强、变现性高<br>
          直接投资房地产，金额较高，还要透过中介机构，手续繁琐，且变现性不好。REITs的交易在证券交易所进行，就像买卖股票一样。流动性、变现性都胜过投资不动产。此外，REITs的投资门槛较低，一般投资人都可参与其中。<br>
          4. 税赋优惠<br>
          相比利息收益，REITs收益有更多的税赋优惠。投资REITs每月获得的现金流，至少有一半是本金返还，无需交税。<br>
          5. 简单、便捷<br>
          REITs投资人是很多物业的业主，却没有普通业主的烦恼：不用担心物业空置，不用害怕遇到不良租客，也不会有租客大半夜打电话，投诉网路不通畅或是洗手间漏水。
        </p>



