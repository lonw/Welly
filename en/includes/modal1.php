Meet with your lawyer prior to your closing or possession date. Generally, your real estate lawyer will want to meet with you one month before your closing date. No matter where you are in Ontario, contact us for a list of lawyers in your area.</p>
<h4><b>Step 7:  Move In</b></h4><br>
<p>
  &nbsp;&nbsp;You’ve found the house, you have your mortgage in place, and all conditions have been removed. It’s time to plan your move:
If you plan on hiring a mover, now is the time to do so since movers book up very quickly.
Give notice to your current landlord.
Start the process of contacting your family members and creditors to provide them with your new address.
Call your local utility companies to set up an account as well as informing them of your move in date.</p>

