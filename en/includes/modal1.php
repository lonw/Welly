        <h4><b>Step 2: Arrange Your Down Payment</b></h4><br>
        <p>
          &nbsp;&nbsp;If you are taking advantage of the Home Buyers Plan, you should make sure that you have notified your financial advisor about your plans to purchase. You will then be required to fill out a T1036 form from Revenue Canada. Your financial advisor or tax office can supply you with this form. Receipt of your funds may take some time so it is important that you request your funds early on in your home buying process.
If your down payment is coming in the form of a “Gift,” you will be required to supply proof in the form of a letter that states who is gifting you the money and what it will be used for.</p>
        <h4><b>Step 3: Hire an Experienced Realtor</b></h4><br>
        <p>
          &nbsp;&nbsp;Having an experienced realtor guide you through this market can make the experience of purchasing your home a lot easier. An experienced realtor will be able to help you find the home of your dreams while negotiating the best price for you. Welly Financial partners with some of the top real estate agents in Ontario.</p>
        <h4><b>Step 4: Hire a Home Inspector</b></h4><br>
        <p>
          &nbsp;&nbsp;Home inspectors are a great resource to ensure that your new home purchase is not only a house you get to live in but also a sound and safe investment. With a report from a home inspector, you will be able to see the full extent of any damages or deficiencies that may not be visible in your new home.</p>
        <h4><b>Step 5: Have All Your Documents Ready</b></h4><br>
        <p>
          &nbsp;&nbsp;From your mortgage approval to the purchase contract, there are many documents you will need to sign and provide to complete the transaction. Your Urban Mortgage agent will guide you on the exact documents you will need to obtain a mortgage commitment. Below is a list of the most common documents required:
Employment Letter
Most Recent pay stub
Statements indicating where the down payment is coming from. In the case of a 5% down payment mortgage, lenders may require that you provide three months history on the down payment. With a 5% down payment, you will also be required to provide proof that you have 1.5% of the purchase price for closing costs
Purchase Contract and all amendments
Void cheque
Copy of drivers license and another piece of ID</p>
        <h4><b>Step 6: Hire a Real Estate Lawyer</b></h4><br>
        <p>
